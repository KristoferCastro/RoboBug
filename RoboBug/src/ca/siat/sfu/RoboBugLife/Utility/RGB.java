package ca.siat.sfu.RoboBugLife.Utility;

public class RGB {
	public int red;
	public int green;
	public int blue;
	public int alpha;
	
	public RGB(int red, int green, int blue){
		this.red = red;
		this.green = green;
		this.blue = blue;
	}
	
	public RGB(int red, int green, int blue, int alpha){
		this.red = red;
		this.green = green;
		this.blue = blue;
		this.alpha = alpha;
	}
}
